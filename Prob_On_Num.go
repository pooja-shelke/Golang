//Accept two numbers from user and display first number in second
//number of times.

package main
import "fmt"

func Display(iValue1 int,iValue2 int){

	var i int=0

	for i=1;i<=iValue2;i++{
       
       fmt.Println(iValue1)
	}
}

 func main(){

 	var iValue1 int=0
 	var iValue2 int=0

 	fmt.Println("Enter first Number")
 	fmt.Scanln(&iValue1)

 	fmt.Println("Enter Second Number")
 	fmt.Scanln(&iValue2)

 	Display(iValue1,iValue2)

 }
